class Financa { 

    constructor(item, data, quantidade, valor) { 
        this.item = item
        this.data = data
        this.quantidade = quantidade
        this.valor = valor
    }

    get item() { 
        return this.item
    }

    get data() { 
        return this.data
    }

    get quantidade() { 
        return this.quantidade
    }

    get valor() { 
        return this.valor
    }
}